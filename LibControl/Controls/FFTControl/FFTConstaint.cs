        public static int getFilterWithType(XoverType type)
        {
            int ret = 0;
            switch (type)
            {
                case XoverType.ByPass:
                    ret = 0;    // Bypass
                    break;
                case XoverType.BW6dB:         // Butterworth -6dB/oct
                    ret = 1;
                    break;
                case XoverType.BW12dB:         // Butterworth -12dB/oct
                    ret = 2;
                    break;
                case XoverType.BW18dB:         // Butterworth -18dB/oct
                    ret = 3;
                    break;
                case XoverType.BW24dB:         // Butterworth -24dB/oct
                    ret = 4;
                    break;
                case XoverType.BW30dB:         // Butterworth -30dB/oct
                    ret = 5;
                    break;
                case XoverType.BW36dB:         // Butterworth -36dB/oct
                    ret = 6;
                    break;
                case XoverType.BW42dB:         // Butterworth -42dB/oct
                    ret = 7;
                    break;
                case XoverType.BW48dB:          // Butterworth -48dB/oct
                    ret = 8;
                    break;
                case XoverType.BES6dB:          // Bessel -6dB/oct
                    ret = 9;
                    break;
                case XoverType.BES12dB:        // Bessel -12dB/oct
                    ret = 10;
                    break;
                case XoverType.BES18dB:        // Bessel -18dB/oct
                    ret = 11;
                    break;
                case XoverType.BES24dB:        // Bessel -24dB/oct
                    ret = 12;
                    break;
                case XoverType.BES30dB:         // Bessel -30dB/oct
                    ret = 13;
                    break;
                case XoverType.BES36dB:         // Bessel -36dB/oct
                    ret = 14;
                    break;
                case XoverType.BES42dB:         // Bessel -42dB/oct
                    ret = 15;
                    break;
                case XoverType.BES48dB:         // Bessel -48dB/oct
                    ret = 16;
                    break;
                case XoverType.LK12dB:
                    ret = 17;    // Link/Riley -12dB/oct
                    break;
                case XoverType.LK24dB:
                    ret = 19;     // Link/Riley -24dB/oct
                    break;
                case XoverType.LK36dB:        // Link/Riley -36dB/oct
                    ret = 18;
                    break;
                case XoverType.LK48dB:        // Link/Riley -48dB/oct
                    ret = 20;
                    break;
                default:
                    break;
            }

            return ret;
        }


